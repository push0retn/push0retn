.model	tiny
code	segment
org	100h

;(Формулировка из задачи)
;Написать  программу,  определяющую  номер  позиции,  с  которой  начинается  первое  слева   вхождение заданной конфигурации  символов в исходную строку. 
;Позиция искомой последовательности чисел записывается программой в регистр АХ.

start:

mov si, offset str_source
mov cx, len_source
mov dx, cx			;Копирование размера исходного массива.

;Поиск первого вхождения заданной конфигурации символов.

search:

mov di, offset str_search
repe cmpsb
dec dx				;Вычитание итерации сравнения, в результате которой, массивы не равны.
push dx				;Сохранение регистра данных в стек.
sub dx, cx			;Получение разности регистров счётчика и данных.
cmp dl, len_search	;Сравнение разности с длиной искомой последовательности чисел, 
					;если разность равна длине искомой последовательности, то программа нашла эту последовательность чисел.
je exit
pop dx
mov dx, cx
loop search

;Конфигурация найдена, выход из программы.

exit:

sub si, len_search
mov ax, si
pop dx

mov ax, 4c00h
int 21h

str_source	db	'asfbkjasfbkjqw bruiqw sbfk j asbfjsjjjjsj'		;Исходный массив чисел.
len_source	equ	$-str_source									;Длина исходного массива.

str_search	db	'brui', 24h										;Искомая последовательность чисел.
len_search	equ	$-str_search									;Длина искомой последовательности чисел

code	ends
end	start